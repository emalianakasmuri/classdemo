package demo.simple;

/**
 * This class represents a task for a printer
 * 
 * @author emalianakasmuri
 *
 */
public class StatementPrinter {
	
	/**
	 * This method represent a task carried out by the printer.
	 * It prints a statement based on the number of the counter
	 * 
	 */
	public void printStatement () {
		
		for (int counter = 0; counter < 5; counter++) {

			System.out.println("Java is easy");
		}
	}

}



